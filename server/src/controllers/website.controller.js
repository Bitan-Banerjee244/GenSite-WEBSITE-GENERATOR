import Website from "../models/website.model.js";
import generateCode from "../services/generateCode.js";
import { masterPrompt } from "../utils/masterPrompt.js";

const safeParseJSON = (response) => {
  try {
    if (!response) return null;

    let cleaned = response.trim();

    cleaned = cleaned
      .replace(/^```json\s*/i, "")
      .replace(/^```\s*/i, "")
      .replace(/\s*```$/i, "");

    try {
      return JSON.parse(cleaned);
    } catch {
      const firstBrace = cleaned.indexOf("{");
      const lastBrace = cleaned.lastIndexOf("}");

      if (firstBrace === -1 || lastBrace === -1) {
        throw new Error("No JSON object found");
      }

      const jsonString = cleaned.slice(firstBrace, lastBrace + 1);

      return JSON.parse(jsonString);
    }
  } catch (error) {
    console.log("JSON Parse Error:", error.message);
    return null;
  }
};

const generateSlug = (title) => {
  return (
    title
      .toLowerCase()
      .trim()
      .replace(/[^a-z0-9]+/g, "-")
      .replace(/^-|-$/g, "") +
    "-" +
    Date.now()
  );
};

export const generateCodeResponse = async (req, res) => {
  try {
    const { prompt } = req.body;

    if (!prompt?.trim()) {
      return res.status(400).json({
        success: false,
        message: "Prompt is required",
      });
    }

    const finalPrompt = masterPrompt.replace("{USER_QUERY}", prompt.trim());

    const aiResponse = await generateCode(finalPrompt);

    if (!aiResponse) {
      return res.status(500).json({
        success: false,
        message: "No response generated by AI",
      });
    }

    const data = safeParseJSON(aiResponse);

    if (!data) {
      return res.status(500).json({
        success: false,
        message: "Invalid JSON returned by AI",
      });
    }

    if (
      !data.title ||
      typeof data.title !== "string" ||
      !data.code ||
      typeof data.code !== "string"
    ) {
      return res.status(500).json({
        success: false,
        message: "AI response missing title or code",
      });
    }

    const cleanData = {
      title: data.title.trim(),
      code: data.code,
    };

    const slug = generateSlug(cleanData.title);

    const chat = [
      {
        role: "user",
        context: prompt,
      },
      {
        role: "ai",
        context: cleanData.title,
      },
    ];

    const website = await Website.create({
      user: req.user,
      title: cleanData.title,
      code: cleanData.code,
      slug,
      chat,
    });

    return res.status(200).json({
      success: true,
      website,
    });
  } catch (error) {
    console.error("Generate Website Error:", error);

    return res.status(500).json({
      success: false,
      message: "Error occurred while generating website",
    });
  }
};

export const getAllWebsites = async (req, res) => {
  try {
    let user = req.user;

    if (!user) {
      return res.status(400).json({
        success: false,
        message: "No user found ! Please Login",
      });
    }

    let websites = await Website.find({ user });

    if (!websites) {
      return res.status(400).json({
        success: false,
        message: "No Websites found",
      });
    }

    return res.status(200).json({
      success: true,
      message: "Websites fetched successfully",
      websites,
    });
  } catch (error) {
    return res.json(500).status({
      success: false,
      message: "Error in fetching websites",
      error: error,
    });
  }
};

export const getWebsitesById = async (req, res) => {
  try {
    let id = req.params.id;

    if (!id) {
      return res.status(400).json({
        success: false,
        message: "Id is not provided",
      });
    }

    let website = await Website.findById(id);

    if (!website) {
      return res.status(400).json({
        success: false,
        message: "No Website Found",
      });
    }

    return res.status(200).json({
      message: "Website Fetched",
      success: true,
      website,
    });
  } catch (error) {
    return res.status(500).json({
      message: "Website not found! Error occurred in server",
      success: false,
      error: error,
    });
  }
};
