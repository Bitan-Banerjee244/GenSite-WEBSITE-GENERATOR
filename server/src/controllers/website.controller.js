export const createURL = async (req, res) => {
  try {
    const { id } = req.params;
    if (!id) {
      return res.status(400).json({
        message: "No id found",
        success: false,
      });
    }

    let website = await Website.findById(id);
    if (!website) {
      return res.status(404).json({
        success: false,
        message: "Website not found",
      });
    }

    let url = `${process.env.FRONTEND_URL}/site/${website?.slug}/${website?._id.toString().slice(-5)}`;
    website.isDeployed = true;
    website.shareLink = url;
    await website.save();

    return res.status(200).json({
      message: "Website Deployed Successfully",
      success: false,
      url: website?.shareLink,
    });
  } catch (error) {
    return res.status(500).json({
      message: "Error occurred in create url",
      success: false,
      error: error,
    });
  }
};

export const updateWebsite = async (req, res) => {
  try {
    const { id } = req.params;
    const { prompt } = req.body;
    let userId = req.user;

    // Validate input
    if (!id || !prompt?.trim()) {
      return res.status(400).json({
        success: false,
        message: "Prompt or website id is missing.",
      });
    }

    // Find website
    const website = await Website.findById(id);

    if (!website) {
      return res.status(404).json({
        success: false,
        message: "Website not found.",
      });
    }

    if (!userId) {
      return res.status(404).json({
        success: false,
        message: "User not found.Please Login",
      });
    }

    let user = await User.findById(userId);

    if (user.credits < 50) {
      return res.status(404).json({
        success: false,
        message: "Credits unavailable",
      });
    }

    // Create final prompt
    const finalPrompt = updatePrompt
      .replace("{USER_QUERY}", prompt)
      .replace("{CURRENT_CODE}", website.code);

    // Generate updated website
    const output = await generateCode(finalPrompt);

    // Remove markdown if AI sends it
    const cleanedOutput = output
      .replace(/```json/g, "")
      .replace(/```/g, "")
      .trim();

    let data;

    try {
      data = JSON.parse(cleanedOutput);
    } catch (err) {
      console.log("Invalid JSON from AI:");
      console.log(cleanedOutput);

      return res.status(500).json({
        success: false,
        message: "AI returned invalid JSON.",
        raw: cleanedOutput,
      });
    }

    website.code = data.code || website.code;
    website.chat.push(
      {
        role: "user",
        context: prompt,
      },
      {
        role: "ai",
        context: data?.response,
      },
    );
    await website.save();

    user.credits -= 50;
    await user.save();

    return res.status(200).json({
      success: true,
      message: "Website updated successfully.",
      website: website,
    });
  } catch (error) {
    console.error("Update Website Error:", error);

    return res.status(500).json({
      success: false,
      message: "Error occurred while updating website.",
      error: error.message,
    });
  }
};
